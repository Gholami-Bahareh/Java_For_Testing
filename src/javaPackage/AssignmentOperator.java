package javaPackage;

public class AssignmentOperator {

	public static void main(String[] args) {
		int a = 10;
		a+=5; //+= is a symbol of assignment operator(Don't give space in between + and =) 
		System.out.println(a); //15
		
		a-=5;
		System.out.println(a); 
		
		a*=3;
	    System.out.println(a); 
		
		a/=2;
		System.out.println(a); 
		

	}

}
