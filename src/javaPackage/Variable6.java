package javaPackage;

public class Variable6 {

	public static void main(String[] args) {
	int z = 25; //local variable
	System.out.println(z); //25
	
	int x = 100; //local variable
	System.out.println(x); //100
	
	x = 200; //local variable (overriding value of x)
	System.out.println(x); //200
	
	System.out.println(10>30); //false
	System.out.println(30>10); // true
	
	int f; //declaring a variable
	int g = 20; //defining a variable
	System.out.println(g); //calling a variable to print its value
	
	

	}

}
