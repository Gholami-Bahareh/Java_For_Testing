package javaPackage;

public class UnaryOperator1 {

	public static void main(String[] args) {
	int a = 1;
	System.out.println(a++); //output = 1  ... stored in RAM = 2
	System.out.println(a); // output = 2
	System.out.println(a++); //output 2 .. in the RAM = 3
	System.out.println(++a); // output = 4 ... in the RAM = 4
	

	}

}
