package javaPackage;

public class IfElse3 {

	public static void main(String[] args) {
		int a = 300;
		int b = 300;
		if (a<b) {
			System.out.println("a less than b");
		}
		else if (a>=b) {
			System.out.println("a is greater than b");
		}
		else if (a==b) {
			System.out.println("a is equal to b");
		}
		else {
			System.out.println("a and b are different");
		}

	}

}
